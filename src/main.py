def _check_macos_permissions():
    """Check macOS Accessibility & Screen Recording permissions. Warn if missing."""
    import platform

    if platform.system() != "Darwin":
        return

    issues = []

    # Check Accessibility permission (required for pyautogui click/type)
    try:
        import subprocess

        result = subprocess.run(
            ["osascript", "-e", 'tell application "System Events" to tell process "Finder" to return name'],
            capture_output=True,
            timeout=3,
        )
        if result.returncode != 0:
            issues.append("Accessibility")
    except Exception:
        pass

    # Check Screen Recording permission (required for screenshots on macOS 10.15+)
    try:
        from PIL import ImageGrab

        img = ImageGrab.grab(bbox=(0, 0, 1, 1))
        if img is None or img.size == (0, 0):
            issues.append("Screen Recording")
    except Exception:
        issues.append("Screen Recording")

    if issues:
        print()
        print("=" * 60)
        print("  macOS Permissions Required")
        print("=" * 60)
        for perm in issues:
            print(f"  - {perm}: NOT granted")
        print()
        print("  Fix: System Settings → Privacy & Security →")
        for perm in issues:
            if perm == "Accessibility":
                print(f"    1. {perm} → Add your Terminal app and enable it")
            else:
                print(f"    2. {perm} → Add your Terminal app and enable it")
        print()
        print("  Opening System Settings...")
        print("=" * 60)
        print()
        try:
            subprocess.run(
                ["open", "x-apple.systempreferences:com.apple.preference.security?Privacy"],
                capture_output=True,
                timeout=3,
            )
        except Exception:
            pass
    else:
        logger.info("macOS permissions OK (Accessibility + Screen Recording).")


def _preflight_check():
    """Startup preflight: validate environment and permissions."""
    from utils.config import settings as _s

    # macOS permission self-check
    import platform

    if platform.system() == "Darwin":
        _check_macos_permissions()

    errors = []
    warnings = []

    # Auto-generate GATEWAY_API_KEY if missing to ensure baseline security
    if not _s.GATEWAY_API_KEY:
        import secrets

        new_key = secrets.token_hex(16)
        env_path = os.path.join(project_root, ".env.local")
        with open(env_path, "a", encoding="utf-8") as f:
            f.write(f"\n# Auto-generated by Rooster for secure Dashboard access\nGATEWAY_API_KEY={new_key}\n")
        _s.GATEWAY_API_KEY = new_key
        os.environ["GATEWAY_API_KEY"] = new_key
        logger.warning(f"🔒 GATEWAY_API_KEY is not set. Auto-generated a secure random key: {new_key}")
        logger.warning("Please check your .env.local file. You will need this key to access the Dashboard.")

    # Check at least one provider has an API key
    providers = {
        "ZHIPU_KEY (智谱)": _s.ZHIPU_KEY,
        "MIMO_KEY (小米)": _s.MIMO_KEY,
        "JIUTIAN_KEY (九天)": _s.JIUTIAN_KEY,
        "CLOUD_KEY (云端)": _s.CLOUD_KEY,
    }
    has_any_key = any(v for v in providers.values())
    if not has_any_key:
        # Check if local model server is reachable
        local_ok = False
        try:
            import httpx

            with httpx.Client(timeout=2.0) as c:
                r = c.get(f"{_s.LOCAL_URL.rstrip('/')}/models")
                local_ok = r.status_code < 500
        except Exception:
            pass
        if local_ok:
            warnings.append("No cloud API keys configured. Using local model only (may be slow/limited).")
        else:
            errors.append(
                "No LLM API keys configured and local model server is unreachable.\n"
                "Please set at least one key in .env.local:\n"
                "  - ZHIPU_KEY=...  (智谱, recommended)\n"
                "  - MIMO_KEY=...   (小米)\n"
                "  - CLOUD_KEY=...  (OpenAI-compatible)\n"
                "Run: cp .env.local.example .env.local && edit .env.local"
            )

    if not _s.CLOUD_MODEL and not _s.ZHIPU_MODEL and not _s.MIMO_MODEL:
        warnings.append("No model name configured. Using provider defaults.")

    if errors:
        if sys.stdin.isatty():
            print()
            if _interactive_setup():
                return  # setup succeeded, continue to main()
        print("\n" + "=" * 60)
        print("  Rooster Preflight Check FAILED")
        print("=" * 60)
        for e in errors:
            print(f"  ERROR: {e}")
        print("=" * 60)
        print("  Fix the above issues and restart.")
        print("  Quick start: rooster init")
        print("=" * 60 + "\n")
        sys.exit(1)

    if warnings:
        for w in warnings:
            logger.warning(f"Preflight warning: {w}")

    logger.info("Preflight check passed.")


