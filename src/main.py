_PROVIDERS = [
    ("Zhipu AI", "ZHIPU_KEY", "glm-4-flash", "https://open.bigmodel.cn/api/paas/v4"),
    ("Xiaomi MiMo", "MIMO_KEY", "MiMo-7B", ""),
    ("OpenAI-compatible", "CLOUD_KEY", "gpt-4o", "https://api.openai.com/v1"),
    ("Jiutian", "JIUTIAN_KEY", "", ""),
    ("Local model server", "LOCAL_KEY", "local", "http://127.0.0.1:8080/v1"),
]


def _interactive_setup() -> bool:
    """Interactive setup wizard — prompts for API keys and writes .env.local.

    Returns True if at least one key was saved and preflight passes.
    """
    print("=" * 60)
    print("  Welcome to Rooster!")
    print("  No LLM API keys found. Let's set one up.")
    print("=" * 60)
    print()
    print("  Choose a provider (enter number):")
    for i, (name, key_name, default_model, _) in enumerate(_PROVIDERS, 1):
        print(f"    {i}. {name:25s}  ({key_name})")
    print()

    try:
        choice = input("  Provider [1]: ").strip()
    except (EOFError, KeyboardInterrupt):
        print()
        return False

    if not choice:
        choice = "1"
    try:
        idx = int(choice) - 1
        if idx < 0 or idx >= len(_PROVIDERS):
            raise ValueError
    except ValueError:
        print("  Invalid choice.")
        return False

    name, key_name, default_model, default_url = _PROVIDERS[idx]

    if key_name == "LOCAL_KEY":
        # Local model — no key needed, just check URL
        url = input(f"  Model server URL [{default_url}]: ").strip() or default_url
        env_lines = [
            f"LOCAL_URL={url}",
            f"LOCAL_MODEL={default_model}",
        ]
    else:
        try:
            api_key = getpass.getpass(f"  Enter {name} API Key: ")
        except (EOFError, KeyboardInterrupt):
            print()
            return False
        if not api_key.strip():
            print("  No key entered.")
            return False

        model = input(f"  Model name [{default_model}]: ").strip() or default_model
        env_lines = [f"{key_name}={api_key.strip()}"]
        # Add model name and optional base URL
        model_key = key_name.replace("_KEY", "_MODEL")
        env_lines.append(f"{model_key}={model}")
        if default_url:
            base_url_key = key_name.replace("_KEY", "_BASE_URL")
            url = input(f"  Base URL [{default_url}]: ").strip() or default_url
            env_lines.append(f"{base_url_key}={url}")

    # Write to .env.local
    env_path = os.path.join(os.path.dirname(os.path.dirname(__file__)), ".env.local")
    existing_lines = []
    if os.path.exists(env_path):
        with open(env_path, encoding="utf-8") as f:
            existing_lines = f.readlines()

    # Merge: update existing keys, append new ones
    existing_keys = set()
    for line in existing_lines:
        stripped = line.strip()
        if stripped and not stripped.startswith("#") and "=" in stripped:
            existing_keys.add(stripped.split("=", 1)[0].strip())

    with open(env_path, "a", encoding="utf-8") as f:
        if existing_lines and not existing_lines[-1].endswith("\n"):
            f.write("\n")
        for entry in env_lines:
            k = entry.split("=", 1)[0]
            if k not in existing_keys:
                f.write(entry + "\n")

    print(f"\n  Saved to .env.local ({len(env_lines)} entries)")
    print("  You can edit .env.local anytime to add more providers.\n")

    # Reload env and re-validate
    load_dotenv(dotenv_path=".env.local", override=True)
    return True


async def main():
    """
    Rooster top-level startup entry.
    Uses "dual-driver" mode:
    1. Background Gateway + persistent connection channel.
    2. Console interaction (CLI).
    """
    _preflight_check()

    # Suppress benign ConnectionResetError on Windows ProactorEventLoop pipe close
    if sys.platform == "win32":
        loop = asyncio.get_event_loop()
        _orig = loop.get_exception_handler() or loop.default_exception_handler

        def _quiet_handler(loop, context):
            exc = context.get("exception")
            if isinstance(exc, ConnectionResetError) and getattr(exc, "winerror", None) == 10054:
                return
            _orig(loop, context)

        loop.set_exception_handler(_quiet_handler)

    launcher = RoosterLauncher()

    try:
        # 并发执行网关与终端交互
        # Concurrently execute gateway and terminal interaction
        await asyncio.gather(launcher.launch_gateway(), launcher.launch_cli())
    except (KeyboardInterrupt, SystemExit):
        logger.info("捕获到退出信号。")  # Exit signal caught
    except Exception as e:
        logger.exception(f"Rooster 系统运行中发生致命崩溃: {e}")  # Fatal crash in Rooster system
        # [P1 不死守卫] 关键点：必须以非零码退出，否则守护进程无法感知异常。
        # [P1 Immortal Guard] Critical: must exit with non-zero code, otherwise the daemon cannot detect the anomaly
        sys.exit(1)
    finally:
        # 兜底清理资源
        # Fallback resource cleanup
        await launcher.cleanup()


def main_cli():
    """pip install 后的 CLI 入口点 (pyproject.toml [project.scripts])"""  # CLI entry point after pip install
    if sys.platform == "win32":
        asyncio.set_event_loop_policy(asyncio.WindowsProactorEventLoopPolicy())
        # 修复 Windows 控制台 GBK 编码导致 emoji 日志崩溃
        # Fix Windows console GBK encoding causing emoji log crashes
        sys.stdout.reconfigure(encoding="utf-8", errors="replace")
        sys.stderr.reconfigure(encoding="utf-8", errors="replace")

    # Handle subcommands
    if len(sys.argv) > 1 and sys.argv[1] == "init":
        if _interactive_setup():
            print("  Setup complete! Run 'rooster' to start.")
        else:
            print("  Setup cancelled.")
        return

    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        pass


if __name__ == "__main__":
    main_cli()
